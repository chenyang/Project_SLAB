package logic;

/**
 * Cette classe représente une réservation pour un évenement.
 * @author Anthony
 *
 */
public class Evenement extends Reservation{
	
	/**
	 * L'enseignant demandant la réservation de l'évenement.
	 */
	private Enseignant demandeur;

	@Override
	/**
	 * @return l'enseignant de l'instance
	 */
	public Enseignant getEnseignant() {

		return this.demandeur;
	}

}
